# Exemplo simples de script Python para manipulação de eventos e logs

def log_event(event):
    # Aqui você pode adicionar lógica para armazenar os logs em um arquivo, banco de dados, etc.
    print(f'Log: {event}')

# Exemplo de uso
log_event('Sistema iniciado às 08:00')
log_event('Bomba acionada às 08:30')
log_event('Ajuste de pH realizado às 09:15')
log_event('Última irrigação às 10:00')
